#!/usr/bin/python3
def add(c, b):
    """My addition function

    Args:
        c: first integer
        b: second integer

    Returns:
        The return value. a + b
    """
    return (c + b)

def sub(c, b):
    """My subtraction function

    Args:
        c: first integer
        b: second integer

    Returns:
        The return value. c - b
    """
    return (c - b)

def mul(c, b):
    """My multiplication function

    Args:
        c: first integer
        b: second integer

    Returns:
        The return value. c * b
    """
    return (c * b)

def div(c, b):
    """My division function

    Args:
        c: first integer
        b: second integer

    Returns:
        The return value. c / b
    """
    return int(c / b)
